package org.base;

public class Swapping {
	public static void main(String[] args) {
		int a = 90;
		int b = 30;
		System.out.println("a=:" + b);
		System.out.println("b= :" + a);
		int temp = a;
		a = b;
		b = a;
		b = temp;
		System.out.println("a=:" + a);
		System.out.println("b= :" + b);

	}

}
